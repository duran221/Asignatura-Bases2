/*        CONDICIONALES:

La lógica viene siendo la misma de cualquier lenguaje de programación, tenemos
el común 'IF', 'ELSE' y 'ELSIF'
*/

--Inicio de bloque anónimo:
DECLARE
  --Dos condiciones booleanas para este ejercicio:
	VERDADERO BOOLEAN:=TRUE;
	FALSO BOOLEAN :=FALSE;

BEGIN
  --(IF CONDICION THEN), si la condición es verdadera ingresa a el bloque interno, de lo contrario, no:
  IF VERDADERO THEN
		
		DBMS_OUTPUT.put_line('Condición cumplida');	

  ELSE
		
	  DBMS_OUTPUT.put_line('La condición no se ha cumplido');  
	
	END IF;

--Manejo de excepciones:
EXCEPTION
  WHEN OTHERS THEN
		NULL;

--Final de el bloque anónimo:
END;		  
